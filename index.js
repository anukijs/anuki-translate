const DEFAULT_LANGUAGE = 'en';

/**
 * Supported languages list.
 * @example
 * Translate.languages.forEach(lang => {
 *   console.log(lang); { iso: 'en', name: 'English', nativeName: 'English' }
 * });
 */
Translate.languages = Object.keys(languages).map(function (iso) {
  return { iso, ...languages[iso] }
});

/**
 * Default language.
 * @example
 * Translate.DEFAULT_LANGUAGE
 */
Translate.DEFAULT_LANGUAGE = DEFAULT_LANGUAGE;

const fetch = (function() {
  if (typeof window !== 'undefined') {
    return window.fetch;
  } else {
    return require('node-fetch');
  }
})();

/**
  * translate text
  * @param {string} text Text to translate.
  * @param {string} options.to Translate text to language
  * @param {string} options.from Translate text from language
  * @return {Promise} Promise { pending }
  * @example
  * Translate('hello', { to: 'fr' }).then((data) => {
  *   console.log(data.text);
  *   // => 'Salut'
  * });
*/
function Translate(text, options = {
  from: 'auto',
  to: 'auto'
}) {
  if (typeof text !== 'string') {
    throw new Error('text must be a string, Provided ' + typeof text);
  }

  if (typeof options !== 'object') throw new Error('options type must be object. Provided: ' + typeof options);
  
  text = text.replace(/\n/g, ' ').trim();
  options.from = options.from || 'auto';
  options.to = options.to || DEFAULT_LANGUAGE;

  if (options.from !== 'auto' && !isSupported(options.from)) throw new Error(`"${options.from}" is not supported.`);
  if (options.to !== 'auto' && !isSupported(options.to)) throw new Error(`"${options.to}" is not supported.`);
  
  options.from = getLanguage(options.from);
  options.to = getLanguage(options.to);
  
  let result = {
    text: null,
    to: {
      language: {
        iso: null,
        name: null,
        nativeName: null
      },
      suggestions: []
    },
    from: {
      text: null,
      language: {
        iso: null,
        name: null,
        nativeName: null
      }
    }
  }

  let url = parseUrl(...arguments);

  return new Promise(function(resolve, reject) {
    fetch(url, { method: 'GET' }).then(response => response.json()).then(function(data) {
      result.text = data[0][0][0].replace(/\n/g, ' ').trim();
      if (
        Array.isArray(data[5]) &&
        Array.isArray(data[5][0]) &&
        Array.isArray(data[5][0][2]) &&
        data[5][0][2].length > 0
      ) {
        result.to.suggestions = data[5][0][2].map(arr => arr[0]);
      }
      result.from.text = text.replace('\n', '');
      
      let toISO = options.to == 'auto' ? DEFAULT_LANGUAGE : options.to;
      let fromISO = options.from == 'auto' ? data[2] || data[8][0][0] : options.from;
      
      result.to.language = {
        iso: toISO,
        ...languages[toISO]
      }
      result.from.language = {
        iso: fromISO,
        ...languages[fromISO]
      }
      
      resolve(result);
    }).catch(reject);
  });
}

Translate.translate = Translate;

/**
 * Parse google translate api url
 * @param {string} text Text to translate.
 * @param {string} options.to Translate text to language
 * @param {string} options.from Translate text from language
 * @return {string} url
 */
function parseUrl(text, options = { from: 'auto', to: 'auto' }) {
  let url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=${options.from}&tl=${options.to}&hl=${options.to}&oe=UTF-8&ie=UTF-8&otf=1&dt=at&ssel=0&tsel=0&kc=7&dt=t&q=${encodeURIComponent(text)}`;
  return url;
}

Translate.parseUrl = parseUrl;

/**
 * is supported language
 * @param {string} language Language code
 * @return {boolean}
 * @example
 * isSupportedLanguage('en');
 * // => true
 */
function isSupported(language) {
  if (language.toLowerCase() == 'auto') return true;
  
  let keys = Object.keys(languages);
  let filtered = keys.filter(function (key) {
    return (
      key.toLowerCase() == language.toLowerCase() ||
      languages[key].name.toLowerCase() == language.toLowerCase() ||
      languages[key].nativeName.toLowerCase() == language.toLowerCase()
    )
  });
  
  return !!filtered.length;
}

Translate.isSupported = isSupported;

/**
 * get language iso
 * @param {string} language Language name
 * @return {string}
 * @example
 * getLanguage('english');
 * // => 'en'
 */
function getLanguage(language) {
  if (language.toLowerCase() == 'auto') return 'auto';
  
  let keys = Object.keys(languages);
  let filtered = keys.filter(function (key) {
    return (
      key.toLowerCase() == language.toLowerCase() ||
      languages[key].name.toLowerCase() == language.toLowerCase() ||
      languages[key].nativeName.toLowerCase() == language.toLowerCase()
    )
  });
  
  return filtered[0] || null;
}

Translate.getLanguage = getLanguage;

